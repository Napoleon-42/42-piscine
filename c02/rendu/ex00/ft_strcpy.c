#include <unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i] - 48;
		i++;
	}
	*(dest + i) = *(src + i);
	return (dest);
}
