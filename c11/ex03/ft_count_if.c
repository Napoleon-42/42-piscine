int	ft_count_if(char **tab, int lenght, int (*f)(char*))
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (i < lenght)
	{
		if (f(tab[i]) != 0)
			res++;
		i++;
	}
	return (res);
}
